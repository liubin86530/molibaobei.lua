-- 升级多一点bp
local function moreBP(n)
  return function(charIndex) 
    local originPoint = Char.GetData(charIndex,CONST.CHAR_升级点)
    Char.SetData(charIndex,CONST.CHAR_升级点,originPoint+n)
  end
end
-- id 7 的雇佣函数
local function recruit(charIndex)
  local charLevel = Char.GetData(charIndex,CONST.CHAR_等级)
  if charLevel<80 then
    NLG.Say(charIndex,-1,"80级以上再来找我",CONST.颜色_红色,0)
    return false
  end
  return true
end

